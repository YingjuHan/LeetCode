class Solution {
public:
    int nthUglyNumber(int n) {
        vector<int> factors = {2, 3, 5};
        unordered_set<long> seen;
        priority_queue<long, vector<long>, greater<long>> heap;
        seen.insert(1);
        heap.push(1);
        int u = 0;
        for (int i = 0; i < n; i++) {
            long curr = heap.top();
            heap.pop();
            u = curr;
            for (int j : factors) {
                long next = curr * j;
                if (!seen.count(next)) {
                    seen.insert(next);
                    heap.push(next);
                }
            }
        }
        return u;
    }
};
