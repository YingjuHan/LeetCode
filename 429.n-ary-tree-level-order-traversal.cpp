class Solution {
public:
    vector<vector<int>> levelOrder(Node* root) {
        if (root == nullptr) return {};
        vector<vector<int>> res;
        queue<Node*> q;
        q.push(root);
        while (!q.empty()) {
            vector<int> vals;
            int size = q.size();
            for (int i = 0; i < size; i++) {
                Node* node = q.front();
                q.pop();
                vals.push_back(node->val);
                if (node->children.size() > 0) {
                    for (auto c : node->children) {
                        q.push(c);
                    }
                }
            }
            res.push_back(vals);
        }
        return res;
    }
};
